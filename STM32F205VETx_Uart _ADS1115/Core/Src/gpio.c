void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {


    if(GPIO_Pin == KEY_Pin) {
        if(HAL_GPIO_ReadPin(KEY_GPIO_Port,KEY_Pin) == RESET) {
            HAL_Delay(1);
            if(HAL_GPIO_ReadPin(KEY_GPIO_Port,KEY_Pin) == RESET) {
                KeyState = KEY_DOWN;
            } else {
                HAL_Delay(1);
                if(HAL_GPIO_ReadPin(KEY_GPIO_Port,KEY_Pin) == SET) {
                    KeyState = KEY_UP;
                }
            }
        }


    }
}
